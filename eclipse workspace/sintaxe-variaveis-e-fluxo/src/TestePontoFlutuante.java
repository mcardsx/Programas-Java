
public class TestePontoFlutuante {
	
	public static void main(String[] args) {
		
// Para trabalharmos corretamente com float:
		
		double divisao = 5.0/2;
		
		System.out.println(divisao);
		
// Para interpretarmos um float como inteiro (casting):	
		
		int valorInterpretadoComoInteiro = (int) divisao;
		
		System.out.println(valorInterpretadoComoInteiro);	
		
	}

}
